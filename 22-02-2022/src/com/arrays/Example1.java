package com.arrays;

public class Example1 {

	public static void main(String[] args) {


		int employeeIds[] = {2829, 2101, 2323, 4563, 2192,3045,4566,5666};
		
		/**
		 * From the above statement line 9 indicates us
		 * 
		 * it is an array, with this concept we can able store multiple values in one
		 * variable.
		 * 
		 * array works based on index concept.
		 * 
		 * => array index starts with 0(zero) and ends with length-1
		 * 
		 * For Example:
		 * 
		 * int employeeIds[] = {2829, 2101, 2323, 4563, 2192,3045,4566,5666};
		 *                                             0            1             2          3         4       5          6             7
		 * array length/size is 5
		 * 
		 */
		
		System.out.println(employeeIds[6]);
		
		
	}

}
